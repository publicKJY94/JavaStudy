package class04;

// 반복문 for문
//  : 반복되는 횟수를 정확하게 알때
//  : N번,N회,a부터~b까지,범위,...
public class Test01 {
	
	public static void main(String[] args) {
		
		int i;
		// for(초기식;조건식;증감식)
		for(i=0;i<3;i++) {
			System.out.println("i= "+i);
		}
		
		// 변수 i는 for문 내부에 선언되었기때문에,
		// 해당 구역(지역,블럭)을 넘어가면 더이상 사용 xxx
		// 변수의 scope ☆
		
		System.out.println("i= "+i);
		
		
		
		
		
		
		
		
		
		
		
	}
}
