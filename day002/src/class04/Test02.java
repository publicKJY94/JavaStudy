package class04;

public class Test02 {

	public static void main(String[] args) {
		// 안녕하세요! :D 를 3번 출력해주세요
		for(int i=0;i<3;i++) {
			System.out.println("안녕하세요! :D");
		}
		
		// 1부터 10까지 출력해주세요
		// a부터~b까지
		for(int i=1;i<=10;i++) {
			System.out.print(i+" ");
		}
		
		// 5부터 9까지 출력해주세요.
		System.out.println();
		for(int i=5;i<=9;i++) {
			System.out.print(i+" ");
		}
		
		
	}

}
